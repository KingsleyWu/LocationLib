package com.joywe.locationlibrary.ip;

public class IP {

    /**
     * ip : 61.144.175.232
     * address : 广东省深圳市南山区 /福田区电信
     */

    private String ip;
    private String address;

    public void setIp(String ip) {
        this.ip = ip;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getIp() {
        return ip;
    }

    public String getAddress() {
        return address;
    }
}
